class String21 
{
	public static void main(String[] args) 
	{
		int x=30;
		int y=036;//to convert Octal to decimal use '0'
		int z=0x1e;//to convert Hexa decimal to decimal use 0x or 0X
		int a=0b11110;//to convert Binary to decimal use 0b or 0B
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(a);
	}
}